function queryManagedSelectorMap(selectorMap, documentApi = globalThis.document) {
  return Object.fromEntries(
    Object.entries(selectorMap).map(([key, selector]) => [key, documentApi.querySelector(selector)])
  );
}

export function queryManagedEditorDom(documentApi = globalThis.document) {
  return {
    ...queryManagedSelectorMap(EDITOR_ELEMENT_SELECTORS, documentApi),
    controls: queryManagedSelectorMap(EDITOR_CONTROL_SELECTORS, documentApi)
  };
}

